        <ul class="initiative-bullet-list">
          <li><strong>Mobile Learning Bridge Programs:</strong> Deploying vehicles and local learning centers equipped with interactive educational materials to transition children from slums into formal municipal schools.</li>
          <li><strong>After-School Support Centers:</strong> Establishing free evening tuition centers inside slum communities to help students with homework, exams, and basic language proficiency.</li>
          <li><strong>Supply Kit Distributions:</strong> Providing textbooks, backpacks, stationery, and school uniforms to underprivileged students, removing the immediate financial barrier for parents.</li>
          <li><strong>Basic Digital Literacy:</strong> Setting up portable computer tablets and learning labs to introduce basic typing, internet safety, and interactive multimedia educational courses.</li>
        </ul>
